package _7_SQLite数据库;

public class _1_bean数据 {
	private Integer id;
	private String name;
	
	public _1_bean数据(Integer id, String name) {
		super();
		this.id = id;
		this.name = name;
	}
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
}
